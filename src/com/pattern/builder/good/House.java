package com.pattern.builder.good;

public class House {
    private String base;
    private String wall;
    private String roof;

    public String getBase() {
        return this.base;
    }

    public void setBase(String base) {
        this.base = base;
    }

    public String getWall() {
        return this.wall;
    }

    public void setWall(String wall) {
        this.wall = wall;
    }

    public String getRoof() {
        return this.roof;
    }

    public void setRoof(String roof) {
        this.roof = roof;
    }

}
